package Temel;
public class OndalikliSayiVeritipi {
    public static void main(String[] args) {
        float vFloat = 1;
        //float veri tipine ondalıklı sayı yazdığımızda sonuna türünün float olduğunu belirlemek için f koyulması gerekiyor.
        float vPi = 3.14f;

        double vDouble = 3.14;
        
        System.out.println(vFloat);
        System.out.println(vPi);
        System.out.println(vDouble);
    }
}
