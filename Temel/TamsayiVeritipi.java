package Temel;
public class TamsayiVeritipi {
    public static void main(String[] args) {
        //Byte veri türü 8bit max 127 min -128
        byte vByte = 100; 
        
        //Short veri türü 16 bit 2 üzeri 16 değer alır
        short vShort = 1000;

        //Integer veri türü 32 bit 2 üzeri 32 değer alır
        int vInt = 10000;
        
        //Long veri türü 64 bit 2 üzeri 64 değer alır
        long vLong = 100000;
        System.out.println(vByte);
        System.out.println(vShort);
        System.out.println(vInt);
        System.out.println(vLong);
    }
}
